	<div class="ui somep">
		<p>北京时间3月23日，休斯顿火箭主场100-96战胜活塞。哈登本场比赛砍下21分率队逆转，其中加时赛独揽10分奠定胜局。赛前，哈登夺得MVP的赔率为1赔1.10，本场赛后MVP盘口被博彩公司强行关闭。可以说，哈登夺得MVP几乎可以说是板上钉钉。</p>
 
　　<p>本赛季的哈登场均砍下31.2分，真实投篮命中率达到62.5%，历史上唯一一位能和他平起平坐的球员，是2013-14赛季的凯文-杜兰特，那一年杜兰特场均得到32.0分，真实投篮命中率为63.5%。这种抢眼的表现也让主帅德安东尼不吝溢美之词，现年66岁的老帅在接受接受《今日美国》记者萨姆-艾米克的采访时表示，“他是我见过的最擅长进攻的球员。”</p>
 
　　<p>据NBA官网统计，过去三个赛季，哈登单打频率或是每回合单打得分遥遥领先联盟其他球员（本赛季哈登这两项统计的数据分别为33.3%以及1.24分）。本赛季的哈登正在用他超神的表现向世人宣示，他应该是本季MVP毫无争议的人选。</p>
	<div class="ui img"><img src="http://www.hibet.cc/uploadfile/2018/0323/20180323020005416.png"></img></div>
	<p>赛季初期的MVP榜单上，哈登夺魁的赔率仅为1赔11，如今的赔率骤降直至被博彩公司关闭，可以说，哈登本赛季至今的表现可以说是实实在在的打了博彩公司的脸，连博彩公司都不得不屈服在“大胡子”的超神表现之下。</p>
