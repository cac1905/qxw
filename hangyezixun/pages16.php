		<p>根据目前最新的夺冠赔率来看，博彩公司认为在热身赛完成复仇的巴西加冕大力神杯的几率最高，为1赔5.00，德国以1赔5.50紧追不舍，这两队的实力与底蕴的确略胜一筹；近期热身赛表现飘忽的法国队以1赔7排名第三，拥有伊斯科、科斯塔等实力球员的西班牙夺冠赔率降至1赔8暂列第四，刚刚经历惨败的阿根廷则1赔10列第五。这几支球队构成本届世界杯的争冠第一集团。</p>
		<p>其他球队中，欧洲红魔比利时1赔12依旧列第六，三狮军团英格兰1赔19列第七，C罗领衔的葡萄牙夺冠赔率仅为1赔26，东道主俄罗斯的赔率飙升至1赔41。</p>
