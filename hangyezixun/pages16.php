		<p>今日凌晨，国家队热身赛展开争夺，其中西班牙主场6比1狂胜阿根廷，德国0-1负于巴西。博彩公司也随即更新了世界杯夺冠赔率，最新赔率数据显示，巴西被视为头号夺冠热门，以1赔5.00升至第一，德国1赔5.50紧随其后，法国列第三，赔率为1赔7.00，西班牙则1赔8.00列第四，阿根廷依旧为1赔10排名第五。</p>
