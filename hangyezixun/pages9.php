	<div class="ui somep">
		<p> 拉斯维加斯，是美国内华达州的最大城市，以赌博业爲中心的庞大的旅游、购物、度假産业而着名，世界上十家最大的度假旅馆就有九家是在这里，是世界知名的度假圣地之一，拥有“世界娱乐之都”和“结婚之都”的美称，这座超现代化的大赌窟，平均每年接待世界各地的赌客达2000万人次，另有6.7万具被称爲“吃角子老虎机”的赌器，遍布在城市各个角落。</p>
		<img src="http://www.hibet.cc/uploadfile/2018/0203/20180203104253536.jpg">
		<p> 摩纳哥，袖珍小国，弹丸之地，1856年摩纳哥亲王Charles三世爲解决财政危机，在旧市区北边岬角上开设了首家赌场，爲纪念这位行事极端毁誉参半的亲王，该地区被命名爲蒙特卡罗，从此摩纳哥渐渐成爲欧洲最富有的国度之一。</p>
		<p>大西洋城，是美国最大的赌城之一，是东海岸一个着名的旅游观光城市，赌场当然是吸引衆多游客的最主要原因，据说也是美国小姐的主要赛场之一，大西洋城作爲美国“名城”，显然是得赌业之赐，70年代后期，大西洋城进入蓬勃发展期，不到10年时间就成爲拉斯维加斯强有力的竞争对手，并且大有后来者居上之势。</p>
		<img src="http://www.hibet.cc/uploadfile/2018/0203/20180203104304865.jpg">
		<p> 在约翰内斯堡西北方向大约250公里处，隐藏着一个神秘、令人充满幻想的世外桃源—太阳城，太阳城有南非最大之一的赌场，每年都定期举办各类博采大奖赛，如21点的冠军可获高达100万兰特的奖金，所有参赛者都可以免费享用太阳城内的宾馆，赌场内有各种玩法的老虎机，游客都会因它内部老虎机的摆放数量之多而吃惊。</p>
		<img src="http://www.hibet.cc/uploadfile/2018/0203/20180203104316178.jpg">
		<p>  澳门是一个国际自由港，是世界人口密度最高的地区之一，其着名和娱乐场使澳门长盛不衰，成爲全球最发达、富裕的地区之一，虽然从业人员不到1万人，但它却开辟了澳门财政收入一半以上的来源，而作爲澳门11家赌场的龙头老大—葡京赌场，高尔夫娱乐城更像一台印钞机，一个月的营业收入就高达近三十亿澳门元，而每天进出葡京赌场的顾客平均3万人次之多，不论是顾客人数还是收入，都雄居世界所有赌场之首。</p>
		<img src="http://www.hibet.cc/uploadfile/2018/0203/20180203104332386.jpg">
		<p> 设在华克山庄内的博彩娱乐场是汉城惟一的博彩娱乐场，也是韩国最大的一个赌场，每年吸引40万外国游客前来，和拉斯维加斯相比，华克山庄的赌场规模要小一些，但和马来西亚云顶、澳门葡京赌场相比更加高雅，高档。</p>
		<img src="http://www.hibet.cc/uploadfile/2018/0203/20180203104348241.jpg">
		<p>若论规模，越南涂山赌场简直微不足道，它只及澳门赌场的百分之一，而澳门赌场又只及拉斯维加斯赌场的百分之一，然而，这样一个赌场，对于一个刚刚打开国门的社会主义国家来说，其影响非同寻常。</p>
		<img src="http://www.hibet.cc/uploadfile/2018/0203/20180203104404529.jpg">
		<p>许多人说起巴登，就说那是欧洲的拉斯维加斯，其实，这里跟拉斯维加斯完全是两种味道，不错，巴登也许是欧洲最大的赌城，但它也是世界上最美的赌城，甚至可能是最豪华的赌城，每年有60多万富豪从世界各地飞来这里豪赌。</p>
		<img src="http://www.hibet.cc/uploadfile/2018/0203/20180203104413842.jpg">
		<p>马来西亚是个禁赌的国家，惟有云顶赌场例外，到云顶就意味着赌博，因爲这里有亚洲第二大赌场，24小时恭候世界各地的客人，云顶赌场海外驰名，据说即使在金融风暴期间云顶赌场的股票也始终立于不倒的地位，云顶赌场的开办人是被誉爲大马赌王的华人林梧桐先生。</p>
