		<p>迪拜虽然是世界奢华之都，但往年阿联酋的赌场并不入流。今年，拜拉斯维加斯枪击桉所赐，一大批顶级赌场退出竞争让这匹黑马爆冷进入榜单，然而也只能排在末尾。</p>		
	</div>
	<div class="ui img"><img src="http://www.hibet.cc/uploadfile/2018/0207/20180207104614723.jpg"></img></div>
	<div class="ui somep">
		<p>这也是来自阿根廷的赌场首次上榜。经济衰退如斯，除了球王梅西之外，阿根廷本没有什么能拿得出手的东西幸好位于门多萨的凯悦酒店赌场进入全球十大赌场排行，爲阿根廷在旅游方面挽回了一些顔面。</p>		
	</div>
	<div class="ui img"><img src="http://www.hibet.cc/uploadfile/2018/0207/20180207104627717.jpg"></img></div>
		<div class="ui somep">
		<p> 新加坡跟阿根廷一样也是首次入围。并不常被各大媒体提起，但马里纳美丽的海滩着实爲这间酒店加分不少！不论输赢，你都可以在小赌一番之后去沙滩上喝杯鸡尾酒，吹吹海风。</p>		
	</div>
	<div class="ui img"><img src="http://www.hibet.cc/uploadfile/2018/0207/20180207104637966.jpg"></img></div>
		<div class="ui somep">
		<p> 伊比萨，世界电子音乐的中心，人们往往会认爲这里总是没完没了的电子乐派对没错，但这并非全部！人们所不知道的是，伊比萨度假酒店里有一间富丽堂皇、极尽奢华的赌场，而且宾客体验非常良好。</p>		
	</div>
