	<div class="ui somep">
		<p>博华太平洋高级副总裁Tsai指出，许多运营商忽略越南的潜力，支持菲律宾和柬埔寨等更受欢迎的东南亚司法管辖区。但Tsai认为，越南博彩市场仍然有很多机会提供。</p>
		<p>“我认为还有渗透的余地。正如我们之前与越南博彩市场谈论的那样，我们没有很好地渗透，“Tsai说道。</p>
		<p>Tsai说，越南的自然资源和旅游景点也是中国大陆优质旅游区的良好卖点。</p>
		<p>不幸的是，博华太平洋行政官员表示，利益相关方和政府有一点点推动真正发展越南基地。</p>
		<p>“这是我绞尽脑汁，我想知道为什么，”Tsai说。“如果这些运营商真正了解越南博彩市场，尤其是胡志明市以外的市场，他们将会努力建立这个市场。”</p> 
​       <p>他还认为，像泰国一样，越南证明东南亚地区的赌场行业还没有达到产能过剩的程度。</p> 
​       <p>“我认为目前市场并没有饱和。即使泰国市场没有渗透。泰国入境是巨大的，每年24到2800万美元。泰国出境也很大，“Tsaid指出。“在泰国出口方面，他们如何被定位？这是需要好好看看，了解他们想要开拓新市场的地方。“</p> 
​       <p>如果有一个建议可以让运营商想要投资东南亚，Tsai说他们应该先看看机场和网站，看看大部分航班的运营地点。他指出，大型机场交通确定市场从旅游角度来看具有良好的基础设施。</p> 
​       <p>“从营销战略的角度来看，你需要采取更全面的方法，说这些是我们想要追求的市场。这就是为什么，以及如何，“Tsai说。</p>
