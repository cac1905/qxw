	<div class="ui somep">
		<p>上面的统计数字清楚地表明，在比赛的最后时刻比前面的时间进球明显偏多。这似乎很和合乎逻辑，在比赛结束时，大多数球员都很累，这会导致他们出现一些失误，无论是身体还是精神上都不如之前的状态。替补球员大多是具有攻击性的球员，因为他们新鲜而有休息时间，所以他们可以有所作为。这使他们比通常更慢，更累的防守者强大的多。更重要的是，比赛的最后结果往往是在比赛的最后几分钟决定的。</p>
	
       <p>庄家公司非常清楚，大部分进球都是在比赛结束时才出现的，因为他们分析的数据与我们的统计数据相同。因此，为什么他们为早期进球和晚期进球提供几乎相同的赔率？从统计角度来看，这看起来像是给下注者留下一个优势，事实上并非如此，这完全是因为庄家公司的一个巧妙的伎俩。</p>
 
       <p>庄家的心理</p>
 
       <p>看看早期进球的时间段，通常比延迟进球（相差几分钟）要长得多，很多时候，球员有28分钟的时间来得分，而他们只有15分钟的时间来进球，庄家公司意识这一点，但这并不意味着我们不能战胜他们。</p>
 
       <p>有一些比赛很有可能会出现晚期进球，那些你肯定会在比赛的最后一节发生很多事情。例如，德比比赛或国家杯和欧洲杯的第二回合比赛，如果两队的比赛水平相似，通常会有一个迟到的进球。特别是如果其中一个队需要进球才能进入下一轮，在他们压力很大的这个时候。欧洲锦标赛或世界杯等主要国际赛事的淘汰赛阶段比赛情况相似，如果这一场比赛能够决定他们某一方球队的命运，那么球员们将尽其所有力量取得进球并赢得胜利，他们必须一直战斗到最后一刻，所以进球的可能性会明显高于平常。</p>
