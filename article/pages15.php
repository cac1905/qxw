		<p>老虎机的玩法虽然简单易学，但并不是随便玩就能赢钱的，需要有一定的技巧，对于还没有掌握技巧的玩家来说，就需要在进赌场玩前做好行前策略，有好的策略才会有赢钱的机会。</p>
		<p>在进入赌场要坐上老虎机之前，玩家必须要对自己做好心理建设，一开始就要告诉自己，这一切的主宰者就是自己，不管赢或是输想要继续停止都自自己一念之间，让自己要冷静地知道目标在哪里，并且明确地给自己一些限制，包含了资本、注码、时间、止赢止损。</p>
		<p>只有思绪可以冷静思考的时候才能主宰自己的命运，切忌在冲动及慌乱中行事。玩家想要破解老虎机，除了心态正确以外，还需要了解老虎机的规律及玩法，为了让下注的时候有所依据，在游戏之前就必须要将所有细致都先设定好，不管注额的金额，都必须要有固定的比例。</p>
		<p>当然每一位玩家的财力都不同，所以设定的目标当然也都不一样。当自己毫无策略及技巧却想跟老虎机一争高下，往往等到失败才发现，这样都已经为时已晚了!</p>
