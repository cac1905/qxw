		攻守注法是基于正收益率原则的一种博彩方法，其精髓在于“连赢要冲，连输要缩”。在赌资有限，因而对金钱的承受能力也有限的情况下，攻守注法是赌客克服人性的弱点、控制博彩中的风险、轻松面对强大的赌场的一种有效方法，不仅与数学有关，也和心理学有关。 
