		<p> 我觉得赌场和人一样，也有情绪的，而赌博就象战争，最终目的是要取胜！所以我赌钱的时候，是从大局出发，不在乎过程，要的是结果，所以我会结合“兵法”，从大局出发，把每一牌的输赢当作打仗的每一个战役，然后采取相应的策略，最终取得胜利！所以我总结了以下几“计”：</p>
		<p>“欲擒 故纵”</p>
		<p>我连赢很多手，他就会“生气”，再连吃我很多手。在它连吃我的时候，我就故意输给它，下最小注，什么牌都要，BUST为止！直到我天生21点或者连续几手20点为止！我会考虑下最大注了，而且会连续下大注。从理性上讲，这样会打乱所谓的软件程序。从感性上讲，这样会让庄家变的“骄傲”，然后让它防不胜防，短时间内取胜！这就是“骄兵之计”！或是“欲擒姑纵”之计！</p>
		<p>“调虎离山”</p>
		<p>我玩百家乐，只要有多手台(注意：是单人多手，而不是多人台！)，我势必会上多手台，如果在使用第一计不成功或是收益不理想的时候，我会使用“调虎离山”之计！第一计针对单手的情况，就是跟庄家肉搏，如果实在是博不过它，太强了，就好象一头下山猛虎，那我会立即“调虎离山”！让它去山下兜风去吧，立即转向多手，一般是上3手，这时候你会发现，“山中无老虎，猴子称霸王”，抓紧时间狠宰一把！没有什么细节，要通过实践去体会，就不多说了。</p>
		<p>“走为上计”</p>
		<p> 这就不多说了，如果一，二两计均失效，走人！留得青山在，不怕没柴烧！</p>
		<p>“顺手牵羊”</p>
		<p>在我每次取得胜利以后，我就会把自己交给自己的运气了，拿出赢利的10%，去拉slots！输了就输了，一旦中了个自我满意的奖，走人！"顺手牵羊"！</p>
		<p>“以逸待劳”</p>
		<p>赌钱要对自己的赌本和资金情况事先做好管理和分配，我比较喜欢做一些不可取高红利的场子，既然红利可取，那赌场就不会轻易让你把红利取走的，所以我现在基本不做可取红利了，全做不可取的高红利，玩起来爽，赢的也爽，输不过一点而已，一般也就2、3百。如果存300送300的可取红利和存300送500的不可取红利让我选择，以前我会选1，现在我肯定选2。因为这样会让我有更大的空间去施展前面几计。</p>
