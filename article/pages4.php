		<p>注码法是指通过押注大小的变化，来减低或削除赌场所占优势的方法，即很多朋友说的“赌榄”。</p>
		<p>有很多骗钱作家，经常推出一些注码法，声称可以利用这些方法，轻易在赌场的纯运气的游戏中取胜，例如百家乐、大小、轮盘、番摊，这些方法都是教你在连续输之后加注，直至赢回一把。这些系统的逻辑，是假定你始终总会嬴一次，到时就可把之前输掉的，即使不全赢回来，也大部份赢回来。</p>
		<p>这种愈输愈加注的方法，的确经常给玩家许多次赢到小利，因而沾沾自喜。然而，只要一次倒楣，那玩家便会把以前嬴的小钱全部输回去。它们是建基于一个错误的概念，就是，久未出现的赛果，即将出现的可能性便愈大，押注便愈有利。这简直是谬论，轮盘或大细每次开出的结果，都是独立性的，不能记忆之前的事吧。过去已发生的，和未来要发生的，都是毫无关连的。换句话说，没有一个赛果是“该发生了”。</p>
		<p>我并不完全反对使用注码法。如你乐于使用就用吧。如果你的目的，是宁愿嬴取多次小钱，偶而输一次大钱，那么用愈输愈加注的系统是可以的。如果你不介意许多次小损失，而想要一次嬴大利的话，那愈赢愈加注将帮上你。虽然如此，最终的输钱预期净值，都会与理论上的赌场占先度接近。</p>
		<p>说到底，所有的注码系统都是没用。如你不信，请参考大英百科全书Brittanica，关于赌博的一个专题：“有一个叫机会的成熟性之学说，或叫蒙地卡罗谬论，是赌客常犯的，就是错误地假设，每次开出的结果不是独立的，并且出现偏低的结果，会于短期内平反过来，得以达致平衡。市面上大部份的系统，主要都是根据这个谬论而发明的;赌场当然很鼓励玩家用这样的系统，同时很欢迎这类忽视机率法规，和不相信赛果独立性的赌客。”</p>
		<p>大英百科全书还提及有关轮盘的：“最久和最常用的注码法，是叫输后加倍法(Martingale)，教你每输一次，注码便加大一倍。这大概源自于轮盘的发明，此后，每天都有删删减减类似的新发明，真是层出不穷。多年过后，这些”必嬴系统“最终都不管用，依然要败给赌场那个不败的系统，那个克服不了的0或00的占先度。”</p>
