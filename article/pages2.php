		<p>亚洲轮盘和国际轮盘有什么不同，这个问题我们可以从投注方式上面就很明显的可以看到。</p>
		<p>亚洲轮盘=押注方式：</p>
		<p>在赌桌上有个下注的表格是以每竖排三个数字，共有12排，1～36依序排列，数字的颜色即和轮盘相同。轮盘压注方式有：直接押注号码、两码押注、竖排三码押注、方形四码。</p>
		<p>押注、二竖排六码押注、十二码押注、直线.押注、红色/黑色押注、奇数/偶数押注和大/小范围押注等。关于押注的方式并没有硬性规定，所以您可以自由选择多种押注。在电脑屏幕的右上方我们随时会将上一局游戏的号码秀出来，通常若有号码久久都末出现，那接下来要出的，概率将会很大。</p>
		<p>国际轮盘押注方式：</p>
		<p>本游戏是采用国际轮盘，盘上共有37个小方格（0至36），国际轮盘的号码，排列方式与亚洲轮盘不同。</p>
		<p>轮盘会顺时针转动，在投注时间结束后，小球会向逆时针方向弹出，并于轮盘上滚动，最后会停在其中一个小方格内。</p>
		<p>游戏目的是预测小球会，停留在那一个颜色，号码或投注号码组合上。</p>
